export function getRandomMap() {
    const num = getRandomInt(0, MAPS.length);
    let map = MAPS[num];
    const reverse = getRandomInt(0, 4);
    switch (reverse) {
        case 0: 
            reverseX(map);
        case 1:
            reverseY(map);
            break;
        case 2: 
            reverseX(map);
        // case 3: оставляем как есть
    }
    return map;
}
function reverseX(map) {
    for (let i = 0; i < map.length; i++) {
        map[i].reverse();
    }
}
function reverseY(map) {
    map.reverse();
}